package meeting_02;

public class CelciusToFahrenheit {
    public static void main(String[] args) {
        
    } {

        //Farenheit {F}= Temperature in degrees Celcius{C}+9/5+32
        System.out.print("30 Celcius is:");
        System.out.print(30.0 * (9.0 / 5.0) + 32.0);
        System.out.println(" fahrenheit");


        //Celcius{°C} = (°F - 32) × 5/9
        System.out.print("86 Farenheit is:");
        System.out.print((86.0 - 32.0) * (5.0 / 9.0));
        System.out.println(" celcius");

    }

}
